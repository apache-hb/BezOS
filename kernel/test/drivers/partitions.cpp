TEST(GptPartitionTest, TwoEntries) {
	km::MemoryBlk ramblk((std::byte*)kGptTwoEntries, sizeof(kGptTwoEntries), km::Protection::eRead);
    km::BlockDevice media(&ramblk);

    auto partitions = km::ParsePartitionTable(media);
    ASSERT_TRUE(partitions.has_value()) << "Error: " << std::to_underlying(partitions.error());
    km::MasterBootRecord mbr = partitions->mbr;
	km::GptHeader gpt = partitions->gpt;

    ASSERT_EQ(mbr.signature, km::MasterBootRecord::kSignature);
	ASSERT_EQ(gpt.signature, km::GptHeader::kSignature);

	ASSERT_TRUE(mbr.partitions[0].isGpt());

	ASSERT_EQ(gpt.guid, sm::uuid::of("B6ACD9C4-0786-294F-A8AC-868F08748F80"));

	EXPECT_EQ(gpt.revision, 0x00010000);
	EXPECT_EQ(gpt.size, 0x5c);
	EXPECT_EQ(gpt.partitionEntrySize, 128);

	auto entries = partitions->gptPartitions();
	ASSERT_EQ(entries.size(), 2);

	ASSERT_EQ(entries[0].guid, sm::uuid::of("E009526A-F306-7D47-A73D-D3D6E040457B"));
	ASSERT_EQ(entries[1].guid, sm::uuid::of("FBF202DC-0622-E547-B33A-F9E4F3FACB12"));
}
