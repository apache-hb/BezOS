TEST(MbrPartitionTest, SinglePartition) {
    km::MemoryBlk ramblk((std::byte*)kSinglePartitionMbr, sizeof(kSinglePartitionMbr), km::Protection::eRead);
    km::BlockDevice media(&ramblk);

    auto partitions = km::ParsePartitionTable(media);
    ASSERT_TRUE(partitions.has_value()) << "Error: " << std::to_underlying(partitions.error());
    km::MasterBootRecord mbr = partitions->mbr;

    ASSERT_EQ(mbr.signature, km::MasterBootRecord::kSignature);

	km::MbrPartition p0 = mbr.partitions[0];
    EXPECT_TRUE(p0.isActive());
	EXPECT_EQ(p0.chsBegin, km::ChsAddress::ofChs(4, 4, 1))
		<< std::to_string(p0.chsBegin.head) << ", "
		<< std::to_string(p0.chsBegin.cylinder()) << ", "
		<< std::to_string(p0.chsBegin.sector());

	EXPECT_EQ(p0.chsEnd, km::ChsAddress::ofChs(1, 514, 2))
		<< std::to_string(p0.chsEnd.head) << ", "
		<< std::to_string(p0.chsEnd.cylinder()) << ", "
		<< std::to_string(p0.chsEnd.sector());

    EXPECT_FALSE(mbr.partitions[1].isActive());
    EXPECT_FALSE(mbr.partitions[2].isActive());
    EXPECT_FALSE(mbr.partitions[3].isActive());
}

